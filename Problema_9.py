# Date trei numere, să se calculeze toate sumele posibile de câte două numere. 
# Afişarea să cuprindă şi termenii sumei, nu numai valoarea ei. 
# Exemplu: Date de intrare: 2   13   4  Date de ieşire: 2+13=15  2+4=6  13+4=17. 

a=int(input("introduceti primul numar:"))
b=int(input("introduceti al doilea numar:"))
c=int(input("introduceti al treilea numar:"))
print(a,"+",b,"=",a+b,";",a,"+",c,"=",a+c,";",b,"+",c,"=",b+c)